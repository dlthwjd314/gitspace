package UML;

import java.util.*;

public class Inventory {

	private String invenName;
	private String invenAddr;
	private String invenPhone;
	
	public String getInvenName() {
		return invenName;
	}
	public void setInvenName(String invenName) {
		this.invenName = invenName;
	}
	public String getInvenAddr() {
		return invenAddr;
	}
	public void setInvenAddr(String invenAddr) {
		this.invenAddr = invenAddr;
	}
	public String getInvenPhone() {
		return invenPhone;
	}
	public void setInvenPhone(String invenPhone) {
		this.invenPhone = invenPhone;
	}
	

}
